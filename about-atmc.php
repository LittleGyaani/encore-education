									<h3>ABOUT ATMC AUSTRALIA</h3>
									<p>As a frontrunner in international education, the Australian Technical & Management College (ATMC) provides a pathway for international students seeking higher education in the fields of Business and Management in Australia and abroad.</p>

										<p>First established in the Indian subcontinent, ATMC has evolved into a flourishing nationally recognised education institution in association with three leading Australian universities that specialise in the fields of information technology and business: Federation University Australia (FedUni), Charles Darwin University (CDU) and the University of the Sunshine Coast (USC).</p>

										<p>The college has campuses in Melbourne and Sydney, offering a range of innovative courses designed to prepare students for the global workforce.</p>

										<p>Our campuses are located in some of the most liveable and culturally diverse cities in the world, so our students are guaranteed an unforgettable international study experience. ATMC campuses are warm and inviting, and specifically catered to supporting students studying abroad.</p>

										<p>We offer a range of degrees that are delivered in a personalised and practical learning environment with a strong focus on global industry connections. ATMC students graduate with the skills, knowledge and relevant industry experience necessary to make a significant impact on the world.</p>
										<h3>Quality Courses</h3>
										<p>The ATMC in association with FedUni, CDU and USC offers an exciting collection of bachelor degrees, associate degrees, graduate diplomas, master degrees in Information Technology and Information Systems and bachelor and master degrees in Business and Accounting. There is an absolute commitment to keeping the degree programs offered up-to-date at all times and relevant to the industry environment nationally and internationally. At the ATMC the class sizes are small, so that students enjoy the learning experience and don’t get lost in the crowd. Staff are accessible. At the ATMC students are a name not just a number. In fact, academic and administrative staff get to know the students.</p>
										</br>
										<h3>Modern Equipment</h3>
										<p>Resources are put back into the College to develop facilities and services that benefit students. The College employs contemporary technology in all aspects of the business, delivery and services.</p>
									</br>
									<h3>Excellent Teaching and Sporting Facilities</h3>
										<p>Classrooms are spacious, fully air-conditioned and inviting. The classrooms are equipped with technologically up to date learning and teaching equipment and resources. The ATMC is dedicated to providing the latest and best hardware and software solutions for all students.  From diverse operating systems (such as XP, Linux and UNIX) to high end applications software (such as .NET, Java Enterprise, Case Tools etc). ATMC provides training in all of these areas. All labs are equipped with high-end HP servers, Database management solutions, latest multimedia workstations and wireless devices.</p>
									</br>
									<h3>Student Mentors</h3>
									<p>At the ATMC we recognise that students might need additional help with their academic studies. So at ATMC, we assign mentors to each of our students. This offering is open to all ATMC students, free of charge, whereby they are able to work with high-performing, more senior students, who act as their mentors. These mentors, students with specialised knowledge who have excelled academically, are employed to assist students, whether individually or in small study groups.</p>
								</br>
								<h3>Job Placement Assistance</h3>
								<p>At the ATMC we provide job placement assistance in which complete guidance is provided in terms of resume preparation, mock interviews, etc.  Many students have obtained work placements as a result of ATMC’s assistance.</p>
							</br>

