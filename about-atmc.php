					<h3>ATMC Council</h3>
					<p>Leadership in the College’s governance is a further important aspect of quality. The College’s Council is chaired by Dr John Harvey who, during an illustrious career with IBM, has played a leading role in the founding and operations of the Federation University Australia's Technology Park which is now the largest technology park in regional Australia and in the implementation of an innovative “Earn as you Learn” professional practice higher education degree in information technology.</p>
				</br>
				<h3>The Future</h3>
					<p>The College’s strategic vision has been to expand the ATMC’s operations into the international sphere and a considerable amount of resources and energy has been dedicated to the pursuit of this goal.</p>

					<p>The established ‘base’ in India with ATMC’s Australian IT Skills Colleges will progressively expand into neighbouring Pakistan and Sri Lanka. Further target countries and regions include China, the Middle East and Switzerland, Eastern Europe and South America. Other locations are under active consideration, which will in due course contribute toward an international network of campuses with core values and academic offerings, each ‘tailored’ to respond to the particular international location.</p>

					<p>With these opportunities in mind, the College’s marketing team has been expanded correspondingly to provide ‘face to face’ exchanges between international partners and the authorities that determine what programs can be offered by whom in their jurisdictions.</p>

					<p>The descriptor of ‘College” is an apt one for the ATMC, as it recognizes the ‘collegiate’ attitude between fellow staff and their students. It also connotes the caring, interest and concern shown by staff towards the student body be it through ready personal access, social intermingling or the wide-spread provision of specialized services that support settling into the Australian culture, developing English language skills, providing career advice and the like.</p>

											<p>The aim is not just to make a successful business, or even an academic institution that is recognized for excellent outcomes, but to create a place where the families of international students will be befriended, supported and encouraged as important human beings who are welcomed into the ATMC family. At the completion of their ATMC studies our students will in turn go on to serve future generations. Making a positive difference throughout their professional careers in their countries of origin and elsewhere around the globe will be the ‘hall mark’ of their ATMC educational experience.</p>
