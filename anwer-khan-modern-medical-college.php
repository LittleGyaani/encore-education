									<h3>About Anwer Khan Modern Medical College</h3>
									<a href="#"><img src="https://upload.wikimedia.org/wikipedia/en/thumb/0/01/Anwer_Khan_Modern_Medical_College_logo.png/220px-Anwer_Khan_Modern_Medical_College_logo.png" alt="Anwer Khan Modern Medical College" height="auto" width="auto" /></a>

									<p>The Anwer Khan Modern Medical College, is located in Dhanmondi , Dhaka at the heart of the capital city of Bangladesh. The campus includes 4 six storied modern buildings and an annexe space close by. Adjacent to the college building Batayan 1 and 2 are used as hospitals with general wards , consultant chambers , ICU , Operation Theatres , modern diagnostic lab etc.

AKMMC offers a 5 year MBBS Program approved by the BMDC and affiliated to the University of Dhaka. It is also permitted to operate by the Ministry of Health and Family Welfare , Govt of Peoples Republic of Bangladesh.

With full time academic staff comprising of professors , Asst Professors , Assoc.Professors and 32 lecturers AKMMC is poised to provide quality medical education at an affordable cost.

The curriculum is modeled as per the requirements of the Bangladesh Medical & Dental Council and closely tallies with that of the Medical Council of India. The First Professional MBBS Course is for 1 and ½ years from January to July of the next year. The 2nd and 3rd Professional MBBS spans 1 year each and the 4th and Final spans 1 and ½ years.

The College is affiliated with the University of Dhaka since 2008.</p>
