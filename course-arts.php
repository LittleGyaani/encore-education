<h4>ARTS:</h4>
<p>A social science education gives you a broad and diverse range of subjects and a wide range of career options. Known as an Arts Degree in Australia, a Social Science Degree in the US and a Humanities Degree in the UK, you can design your course to suit your own interests. Subjects vary widely – from foreign languages and linguistics, to literature, politics and psychology degrees, as well as creative arts and the media. You can choose an arts course that reflects your interest, or you can think about the area you would like to work in after you graduate – such as journalism, translation or the government. Most bachelor of arts subjects will broaden, not narrow, your career options – employers regard a liberal arts degree highly, as it teaches you to be open-minded and analytical. And you can always learn job-specific skills later. In the US, a liberal arts college offers a general approach to further education, and social science subjects include economics as well as international relations and political science. In the UK, the wealth of creative arts and cultural funding encourage students to explore cultural studies, history and literature.
</p>
<h2>Admission Procedure</h2>
<p>Entry requirements vary widely between universities and between countries, so be sure to check the information provided by your prospective university before submitting anything. Speaking generally, however, if you are applying for an undergraduate degree you will be asked to show that you have completed your secondary education to a standard that is in line with the required grades (e.g. your GPA, A-level grades or equivalent) for the program you’re applying to. If you have an international qualification and are unsure whether this is accepted, you should contact the admissions department of the university. For non-native English speakers wanting to study in English-speaking countries, it is also highly likely that you’ll need to provide proof of your English-language proficiency by taking an English-language test such as TOEFL or IELTS. Similar tests may be required for those studying in other languages. For more information about language tests. You may be asked to provide some supporting documentation as part of your application. Once again, requirements vary depending on the country and university, but international students are often asked to provide the following: Passport photos for identification A statement of purpose CV/résumé Academic references/ letters of recommendation Certificate and transcripts of your secondary education Proof of English-language proficiency (e.g. a TOEFL/IELTS certificate, for schools in English-speaking countries), or other language test Admissions test results (e.g. GMAT/GRE results, for graduate programs)</p>
