<h4>Nursing/Healthcare:</h4>
<p>Nurses and health care professionals are valued members of the health care system who work inclose collaboration with physicians and members of healthcare fraternity in promoting wellness. Health care in general and Nursing in particular, fall among the top 10 occupations that has the largest career growth potential in the years to come.</p>
<p>There are excellent career opportunities for Indian nurses who hold BSc/Diploma with permanent and temporary work available abroad, especially in the western countries. Nurses figure among the long term shortage skill list in many of the developed countries such as Canada, UK, USA, Australia, New Zealand, Ireland, etc. Demand for nurses will continue to increase dramatically within the next few years.</p>
<p>There are many fast track and cost effective options available for nurses who wish to pursue their career abroad. Indian nurses holding Diploma (GNM) can opt for the Australian BN program or the Integrated Masters (BSc + MSc) program in UK to work as registered nurses in Australia and UK respectively.</p>
