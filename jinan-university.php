									<h3>About Jinan University</h3>
									<a href="#"><img src="https://upload.wikimedia.org/wikipedia/en/thumb/b/b5/JNU_logo.png/220px-JNU_logo.png" alt="Jinan University" height="auto" width="auto" /></a>

									<p>Jinan University, is located in the Shipai district of Guangzhou. The total area of the university measures 730,000 sq.mt. Formerly known as Jinan Academy it was founded in 1906 in Nanjing during the Qing dynasty. Having a long history the Jinan Academy, then later renamed as Jinan School was transferred to Shanghai and Fujian before being finally reconstituted in Guangzhou in 1958.

										The College is under the Central Government of the Peoples Republic of China and owing to state support as well as the munificence of it’s benefactors both at home and overseas, from Hongkong, Macao and Taiwan, Jinan University has achieved success in various fields – teaching as well as scientific research.

										Jinan University is a comprehensive, multi disciplinary university with a College of Liberal arts, College of Science & Engineering, College of Economics and of course the Medical College.

										In March 1984 the college was registered with the World Health Organisation and the students have the privelege of taking and the clinical doctor and practioner registration which has raised the status and the reputation of the college.

										In April 1994, the affiliated hospital was designated as a 3rd-degree hospital (the top degree hospital designation in China) and in September 1995 it was appraised by medical authorities of Guangzhou, Guangdong province and China’s leading health organization. Leading health organizations at all levels have agreed that this affiliated hospital complies with WHO and UNICEF health standards; as a result, an infant-care hospital has been set up.

										The year 2005 ushers in an age of unprecedented cooperation between the college and India as the college opens it’s doors to India. The 1st Alumni association of MBBS students was held on June 14th 2015 <a href="http://ischool.jnu.edu.cn/news/2976.html" target="_blank" title="Jinan University News">Click here</a> for details. The mutual cooperation and exchange of ideas as well as the exposure that these students will develop to the Chinese way of life will not only lead to better relations but will also lead to the dissemination of medical information & practices.</p>
