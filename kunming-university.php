Kunming University was honored with the title of “Model University” award by the provincial and municipal governments for it’s remarkable work in campus security and education. The total area of the campus is 300 acres.

The library on the main campus contains 1.7 million books. The total number of staff and faculty members is 1760. Universities and schools from more than twenty countries, such as USA, UK, Japan, India, Thailand, Finland, Singapore, Malaysia, etc, have visited Kunming University and established academic relationships with it.

