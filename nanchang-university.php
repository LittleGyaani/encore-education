									<h3>About Nanchang University</h3>
									<a href="#"><img src="https://upload.wikimedia.org/wikipedia/en/thumb/6/6f/Nanchang_University_logo.png/180px-Nanchang_University_logo.png" alt="Nanchang University" height="auto" width="auto" /></a>

									<p>Nanchang University, the key comprehensive provincial institution and one of the key constructive universities of China supported by the government under the State 211 Project, is located in Nanchang, the beautiful capital city of Jiangxi Province.

											It is entitled by the Ministry of Education of China to enroll foreign students supported by the Chinese government scholarship and it is also one of the Chinese universities where foreign students come to study with the foreign government scholarships. Nanchang University, initially named National Zhong-Zheng University, was founded in 1940. In May of 1993, Nanchang University was re-established by combining the Jiangxi University and the Jiangxi Polytechnic University. In December of 2004, an agreement was signed by the Ministry of Education of China and the provincial government of Jiangxi to co-manage the Nanchang University. In August of 2005, the university merged the Jiangxi Medical College to become today’s Nanchang University.

											The university promotes international exchange and cooperation and now has established stable relationship of exchange and cooperation with more than 30 institutes of higher education, scientific research institutions and administrative departments in over 20 countries.

											Nanchang University has established the International Education College specialized in training foreign students to study Chinese language and engaging in degree teaching of concerned majors. The university has set up the special building for international students accommodation. Since 1991, it has begun to recruit foreign students from the United States, Canada, Australia, Germany, France, Korean, Japan, Malaysia, Pakistan, Laos, Thailand, Burundi, Guinea, Kampuchea, Syria, Tanzania, Cameron, and other countries. The teaching resource is abundant and all the teachers who are arranged in teaching international students Chinese have got the “Aptitude Certification of Teaching Chinese as a Foreign Language”. The college has an advanced administrative level and the scope of recruiting foreign students is being expanded with obvious achievement.</p>
