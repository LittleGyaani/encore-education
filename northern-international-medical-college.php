	<h2>Inpatients</h2>
	<p>This consists of non-paying male and female beds, paying beds and cabins for patients to be admitted. The college and hospital have a well organized system of interns. Assistant Registrars and Registrars are in training positions. Admission of patients covers a wide range of pathology giving excellent exposure for training of students. All admissions are covered by Associate Professors and above Morning and evening rounds as well as weekend rounds are conducted by them on a regular basis in addition they come to the hospital when they are called even during odd hours. Patient care is supported by adequate radiology and pathology services for investigations.</p>
	<h2>Outpatients</h2>
	<p>The hospital has a busy outpatient’s departments, patients may obtain consultation at a minimum charge. There is also a first track system through which patients may be seen by a consultant of choice. Immediately, on payment of a higher charge. there is an evening shift that starts at 3 pm and is open till 9pm on all working days.</p>
	<h2>Emergency</h2>
	<p>The hospital has fully functioning emergency department that provides coverage during the evening, night and during holidays. It has its own set of doctors who are free to seek help from other services.</p>
	<h2>College accommodation</h2>
	<p>The college has a modern multistoried building which accommodates all the departments required for MBBS course. There are adequate amenities for lecture, tutorial and practical classes of various subjects. Besides, there is provision for accommodations of seminar, symposium and other activities.</p>
	<h2>Library</h2>
	<p>The college has a fully air-conditioned rich library with a congenial environment. There is a collection of huge numbers of text and reference books. The books are of latest edition ang high quality and suitable for both undergraduate and postgraduate studies.</p>
