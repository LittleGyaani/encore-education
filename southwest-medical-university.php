													<p>The campus is divided into two parts – Zhongshan campus and Nangao campus, covering 1,044,700 square meters in total.

															The Zhongshan Campus is located on Mt. Zhongshan and is surrounded by the Yangtze River and the Tuo River, with a lot of green camphor trees, birds and flowers.

															The Nangao Campus is newly constructed with modern facilities.

															The teaching buildings cover 150,000 square meters, the dormitories 80,000 square meters, the canteens 11,600 square meters, and the stadium 10,000 square meters, including 1 swimming pool.

															The university has 3 affiliated hospitals, which are: The Affiliated Hospital of LZMC, the traditional Chinese Medicine Hospital of LZMC and the Dental Hospital of LZMC. There is also a nursing school, a Pharmaceutical Factory, 7 non-subordinating hospitals and 56 teaching hospitals which total 20,000 clinical beds.

															The Affiliated Hospitalof Luzhou Medical College is a “National, Class A, Grade III Hospital”, and is one of the biggest and most advanced hospitals in Southwest China. It has 1,500 staff (300 are professors and associate professors) and 60 departments with 1000 clinical beds. Every year it accepts 30,000 inpatients and 500,000 outpatients.

