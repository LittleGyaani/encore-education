									<p>Sichuan Medical College, established in 1951, is one of China’s premier medical colleges.The College spread over 1,044.000 sq metres houses a state of the art campus including within it’s sprawling structure a main building including 17 teaching hospitals. It is situated in the Sichuan Province of China which is a National historical and cultural city as also a National Clean city. The Zhongsan campus is located on mount Zhongsan surrounded by the Yangtze & Tuo rivers and is a green oasis.

										For the past 53 years LMC has developed into a model of modern teaching, medical care and scientific research. It is a cradle for those students interested in the medical service of the world.

										The college has a strength of 17,000 pupils from all over China and from many foreign countries including Germany, Japan, India etc. There are 2,560 staff members among which 520 are professors & associate professors. They are mainly working in the fields of Western medicine, traditional Chinese medicine and the two areas combined.

										It has 5 non subordinating hospitals and 39 teaching hospitals with a total of 15,500 patient beds.The college has international student exchange & scientific research exchange programs with over 9 countries among them Germany, Japan, USA and the neighboring SAARC countries.</p>
