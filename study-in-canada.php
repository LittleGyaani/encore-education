									<h4>Education System</h4>
										<p>The education system in the Maple Leaf Country is quite easy to understand. </br></br>

											The education system in each of the 13 provinces in Canada is governed by the local rules and regulations. There are various post-secondary institutions that include colleges, universities and technical schools.</br></br>

											Each post-secondary school has its own set of rules on how to apply, and decides what level of English you will need to be at to be accepted, as well as other entry requirements for various courses of study.</br></br>

											Besides this, there are private career and vocational schools which are not necesssarily governed by provincial governments. There are career colleges, community colleges -- both of which are non-degree granting institutions offering courses ranging from 1-3 years. The fee is generally lower than the university or university colleges.</br></br>

											Then there are university colleges offering four-year undergraduate degrees as well as graduate courses ranging from Masters to doctoral. Lastly, there are universities generally offering primarily masters and research programmes only.</br></br>

											To decide which institution to apply to and to ascertain your eligibility, one should contact the college or university directly or better still ask a Pyramid counsellor for seeking advice on the same.</br></br>

											The Pyramid counsellor will be able to guide you on the basis of your academic record, extra curricular activities, your performance in test like IELTS your financial background etc. Once you have chosen a place to study you will need to apply to that school, college or university.</br></br>

											Once the student receives his/her letter of acceptance from the educational institution, he/she will need to apply for the student permit. The time needed to process an application to study in Canada may be different at various visa offices. One does not need a study permit if you plan to take a course or programme in Canada that lasts six months or less. You must complete the course or programme within the period authorised for your stay in Canada.</p></br>
