					<h5>Registration:</h5>
					<p>As soon as you have received notification of admission from the International Office and taken the language test, you can register as a student at the respective institution of higher education (you will be given a registration number. The notification of admission also tells you by when you should register. Please note that the deadlines are often short-term and that you usually have to appear in person! Registration takes place at the Registrar's or Admissions Office. The procedures differ from one institution to another, so get hold of a copy of the information leaflet from the Registrar's Office well in advance. This will tell you which documents you have to bring with you. Certificates, usually the originals with certified translations. In any case, apart from the notification of admission, you will have to produce a health insurance certificate unless you fall into one of the exceptional categories exempt from compulsory insurance cover.
