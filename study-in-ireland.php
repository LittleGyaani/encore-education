									<h4>Education System</h4>
										<p>
											The Irish Education System was traditionally divided into three basis levels: Primary (8 years), Secondary (5 or 6 years) and Higher Education which offers a wide range of opportunities from post-secondary courses, to vocational and technical training, to full degree and the highest post-graduate levels.
									</p>
								</br>
								<p>The higher education system in Ireland is broad in scope and encompasses the university sector, the technological sector, the colleges of education and private, independent colleges. The institutions which fall within the first three groupings are autonomous and self governing, but substantially state funded. Education has been a key factor in making Ireland one of the fastest growing economies in the world over the past decade.</p>
							</br>
								<p>Cooperative Education involves placement of students in employment relevant to their Degree programmes for six or eight months during their undergraduate studies. Some Institutions in Ireland adopts this model as an integral part of the academic programme.</p>
