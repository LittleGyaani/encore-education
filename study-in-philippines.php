										<h4>Medical Education</h4>
										</br>
									<p>
									Philippines has developed the education system, similar to the USA. Thus after High School (Grade 10) the students join a Bachelors Degree. Those who want to study Medicine does the Pre-Medicine in Bachelor of Science in Biology. After which they sit for National Medical Admission Test (NMAT) exam then they enroll for 4 years medical course known as <b>‘Doctor of Medicine’ (MD)</b>. The MD degree awarded in USA / Philippines is equivalent to MBBS of UK / Commonwealth Countries. Philippines is the world's Second largest exporter of Physicians after India.
									</p>
								</br></br>
									<p>All our partner universities are recognized by Commission on Higher Education and Development (CHED), World Health Organization (WHO) and listed in International Medical Education Directory (IMED), Education Commission For Foreign Medical Graduates (ECFMG). Thus the students are eligible to sit for USMLE and they may appear for Part 1 and 2 in the Philippines. Most medical institution follow the American Style PBL Curriculum. <b>'Doctor of Medicine’ (MD)</b> is recognized by Bangladesh Medical & Dental Council, Medical Council of India, Malaysia Medical Council, Nepal Medical Council, Pakistan Medical & Dental Council and other medical councils upon passing the required Screening Tests.</p>
						</div>

								<div class="tabs__content">
									<h4>Admission Procedure</h4>
									<p>Students with 10+2 or Intermediate education with Physics, Chemistry and Biology as specialization with at least 50% are eligible to study medicine in Philippines. </p>
									<p>MBBS in Philippines is 5.5 years of duration. Indian students with Intermediate education with subjects of Physics, Chemistry and Biology will have to join the B.S + M.D program. Before commencing the medicine in Philippines students will first have to undergo a Pre-medicine course which will be of 16 to 24 months of duration.</p>
								</br></br>
								<p>Upon successful completion of this course, students will be awarded a Bachelor of Science degree. The students will then have to clear the National Medical Aptitude Test (NMAT) conducted by the government of Philippines. This exam is conducted twice every year. Medical Universities in Philippines offer tutorials for Indian students to facilitate them to pass the NMAT test without any problem.</p>
								<p>Once the student completes of the B.S course and the NMAT test, then he/she will be admitted into the Medical schools to study Medicine in Philippines which will be of 4 years of duration.</p>
							</br></br>
							<h4>Internship and Screening Test by MCI:</h4>
							<p>Indian students who study MBBS in Philippines are required to clear the Screening test conducted by MCI. Up on successful clearance of the screening test and one year Internship / House surgeon in any of the recognized hospitals in India can start practicing as a Doctor in India.</p>
								</div>

