<h4><span style="font-size: 1em;">GRE:</span></h4>
<p>Graduate Record Examinations</p>
<h4>Administered by:</h4>
<p>ETS (Educational Testing Service)</p>
<h4>Required for:</h4>
<p>Who Takes It and Why?<br>
To pursue PG Degree /Masters Degree in any specialisation apart from MBA in USA. There are some universities which accepts GRE for MBA as well. Also ISB, Hyderabad also started accepting GRE for their flagship program.</p>
<h4>Website:</h4>
<p><a href="http://www.gre.com/" target="_blank">www.gre.com</a>&nbsp;or&nbsp;<a href="http://www.ets.org/" target="_blank">www.ets.org</a></p>
<h4>Validity:</h4>
<p>The score is valid for 5 years.</p>
<h4>Reporting:</h4>
<p>The score is sent to 4 universities free of cost at the time of GRE exam. ETS charges fees (US $ 27 / recipient) for scores sent to universities later.&nbsp;If the students do not select the score recipient at the time of exam, they must pay US$27 per recipient to have scores sent later.</p>
