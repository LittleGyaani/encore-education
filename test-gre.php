<h4>Mode:</h4>
<p>The Verbal Reasoning and Quantitative Reasoning sections are section-level adaptive, meaning that the first section of the Verbal Reasoning and Quantitative Reasoning measures span a range of difficulty levels, from easy to difficult. The second section of each of the measures is administered based on a test taker’s overall performance on the first section.</p>
<h4>Number of tests:</h4>
<p>Only 1 test can be given in a calender month. A total of 5 tests can be given in a year.</p>
