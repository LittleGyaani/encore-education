<p>A registration no. is given as terms of confirmation.</p>
<h4>Rescheduling:</h4>
<p>An intimation is to be given to the ETS 3 days prior to the examination date. The necessary rescheduling fee has to be paid.</p>
<h4>Cancellation:</h4>
<p>An intimation is to be given to the ETS 3 days prior to the examination date. A cancellation fee is applicable.</p>
<h4>Mode:</h4>
<p>The TOEFL test is Internet Based Test (iBT) in India.</p>
<h4>Number of tests:</h4>
<p>You can take the test as many times as you want, however there has to be a gap of 7 days. You may take the TOEFL iBT test only once in any seven-day period, even if you took the test and canceled your scores. If you test more than once in a seven-day period, your new scores will not be reported and your test fee will not be refunded.</p>
<h4>Scores:</h4>
<p>Total score is out of 120 (Internet Based). If the test is taken more than once, all the scores and the dates are sent to the universities. Scores can be cancelled before coming out of the test.</p>
