									<h3>About Virgen Milagrosa University Foundation</h3>
									<a href="http://www.vmuf.edu.ph/"><img src="https://upload.wikimedia.org/wikipedia/en/thumb/e/ee/VMUFlogonew.jpg/220px-VMUFlogonew.jpg" alt="Virgen Milagrosa University Foundation" height="auto" width="auto" /></a>

									<div class="wpb_wrapper">
<p>Virgen Milagrosa University Foundation (VMUF) is a privately-supported coeducational institution offering courses in the medical and paramedical fields, in liberal and applied arts, sciences, and technical-vocational courses, secondary, elementary and pre-school courses. Since, its inception, VMUF has been concerned with educating young men and women in skills, concepts and values that lead to success in life, while helping them prepare for worthwhile service to GOD and humanity.</p>
<p>The curriculum and activities of VMUF are geared toward helping each student achieve his or her God-given potential. VMUF strives to develop every facet of one’s being mentally, physically, emotionally, psychologically and most important, spiritually.</p>
<p>Virgen Milagrosa University Foundation (VMUF) will chart its course in the next decade with new avenues opened to enhance the manifold services it offers. It will continue to play its role for the promotion and advancement of knowledge for the country’s total development as well as to meet the challenges of changing world by teaching the students not only how to earn a living but more importantly “HOW TO LIVE”.</p>
<p>VMUF will continue to promote a way of life – the way to give and serve others. Hopefully, VMUF students who have imbibed this lifestyle will one day spread it outside and contribute to development of their barrios, towns, provinces, and their country and even of the whole world.</p>
<p>More than 4,000 students / pupils enrolled at VMUF, coming from a variety of national ethnic, social and religious backgrounds. The international student body, encompassing diverse cultural heritages, provides a distinctive cosmopolitan flavor and continuing opportunity to develop close, warm and genuine human relationship.</p>
<p>VMUF covers 19 hectares land area in Long Taloy district, San Carlos City, Pangasinan, about 200&nbsp;km, north of&nbsp;<a href="https://en.wikipedia.org/wiki/Manila">Manila</a>, the capital of the Philippines. The Philippine national games (Palarong Pambansa) was held for the first time in there in 1995.</p>
<p>In 1979, the first 21 medical graduates also scored a 100% passing rate in Philippines Medical Board Exams.</p>
<p>The construction of VIRGEN MILAGROSA MEDICAL CENTER was started in 1972 and it was completed and inaugurated on February 16, 1974. Initially it was a 100-bed capacity with ICU, CCU, and complete laboratory facilities. Currently it is a 400 bedded hospital attached within the campus.</p>
<p>In June 1976, VIRGEN MILAGROSA EDUCATIONAL INSTITUTION (VMEI) INC. was established and other Courses such as Dentistry, Veterinary Medicine, Physical Therapy, BS Nursing, Pharmacy and Radiologic Technology were granted permits to operate.</p>
<p>It was also the birth of the Institutes of Arts and Science and the Secretarial Science and the permit to operate Animal Husbandry, Nutrition &amp; Dietetics, Medical Technology and BS Biology was granted in 1977.</p>
<p>The VM Child Learning Center (CLC) was initiated in 1980 with Nursery and Kindergarten classes. Every year from then on, a new grade was added until all the elementary standards were established in 1986 and meanwhile the Center was given full recognition in 1985.</p>
<p>Virgen Milagrosa University Foundation received an award from PACU-COA for being one of the TOP TEN Institutions with the most number of accredited programs (Category A) in December 13, 2004.</p>
<p>In 2008 VMUF celebrated its 50th Golden Jubilee and has just celebrated it’s 57<sup>th</sup> year of existence.</p>
